# 사용자가 숫자가 아닌 문자열을 입력해도 처리할 수 있는 계산기를 만들어라


def plus(a, b):
    return int(a) + int(b)


def minus(a, b):
    return int(a) - int(b)


def times(a, b):
    return int(a) * int(b)


def devision(a, b):
    return int(a) / int(b)


def reminder(a, b):
    return int(a) % int(b)


def negation(a):
    return -int(a)


def power(a, b):
    return int(a)**int(b)
